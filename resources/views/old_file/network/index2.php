<br/>IPv6
<br/>IP주소의 부족현상을 해결하기 위한 차세대 IP추소체계
<br/>IPv4의 주소 공간을 4배 확장한 것으로 128비트 체계의 16진수로 표기하며, 4개의 16진수를 콜론(:)으로 구분
<br/>IPv4에서는 옵션 필드의 구성이 제한적인데 비해 IPv6에서는 확장헤더를 이용하여 IPv4보다 훨씬 다양하고 안정된 옵션을 사용할 수 있음.
<br/>라우터의 부담을 줄이고, 네트워크 부하를 분산시킴
<br/>보안, 인증, 라벨링, 데이터 무결성, 데이터 비밀성 제공
<br/>특정 흐름의 패킷들을 인식하고 확장된 헤더에 선택사항들을 기술할 수 있음
<br/>
<br/>IPv6종류 : 유니캐스트, 애니캐스트, 멀티캐스트
<br/>유니캐스트 : 한 개의 노드에 대한 통신으로 단일목적지를 지정하는 주소형태로 네트워크의 인터페이스들은 주소부여가 가능한 한 수신지가 되어야함
<br/>
<br/>애니캐스트 : 동시에 복수개의 인터페이스에 주소 부여가 가능한 형태로 해당 주소로 보내진 패킷은 노드 그룹들 중 하나에만 전달됨(라우팅 프로토콜의 거리 측정에 따라 결정)
<br/>
<br/>멀티캐스트 : 여러 개 노드가 수신할 수 있는 특정 송출지 번지를 지닌 형태로 해당 주소로 보내진 패킷은 그룹의 모든 노드들에게 전달됨.
