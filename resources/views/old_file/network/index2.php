<br/><h3>7.통신망</h3>
<br/>
<br/>종합정보통신망(ISDN) - Intergrated Services Digital Network
<br/>음성, 데이터 및 이미지 전송에 동일한 디지털 기술이 적용된 통합정보 시스템
<br/>통신망의 경제성과 효율성을 증대시키고 통신처리 기능을 고도화시킴
<br/>종류
<br/>B채널 - 64KBPS 이하의 가입자 정보전송용 채널(회선교환,패킷교환,전용회선 지원)
<br/>D채널 - 회선교환방식을 위한 신호전송용채널(제어신호, 패킷전송 및 저용량 데이터 전송)
<br/>H채널 - 대용량 가입자 정보전송용 채널(고속팩스, 고음질 음성 서비스)
<br/>
<br/>광대역종합정보통신망(B-ISDN)
<br/>광대역 전송 방식과 광대역 교환방식을 통해 가입자와 서비스 제공자를 연결하고 각종 광대역 및 협대역 서비스를 통합한 디지털 통신망
<br/>광섬유 전송 매체를 이용해 150MBPS-600MBPS 전송속도를 가짐
<br/>비동기 전송모드의 전송방식으로 시분할 다중화 방식을 사용
<br/>기존 N-ISDN의 속도보다 훨씬 빠른 고속의 통신서비스와 광대역의 영상정보 및 초고속 데이터 전송이 가능
<br/>
<br/>
<br/>부가가치 통신망(VAN)
<br/>공중 통신 회선에 교환설비, 컴퓨터 및 단말기 등을 접속시켜 새로운 부가 기능을 제공하는 통신망 기능
<br/>전송기능, 교환기능, 통신처리기능, 정보처리기능
<br/>
<br/>고속 LAN
<br/>1.Gigabit Ethernet (초당 1기가 비트의 전송률) 1000 base -x
<br/>특징
<br/>고속 이더넷(fast Ethernet)에 비해 전송속도는 10배 이상 빠르다.
<br/>케이블 매체는 UTP, Shieded Copper, Optical Fiber 등을 이용
<br/>종류
<br/>1000 BASE -CX : 1Gbps 속도로 2쌍의 STP사용
<br/>1000 BASE -LX : 1Gbps 속도로 2쌍의 장파장 다중 모드 또는 광섬유 단일모드
<br/>1000 BASE -SX : 1Gbps 속도로 2쌍의 단파장 다중 모드 광섬유 사용
<br/>1000 BASE -TX : 1Gbps 속도로 4,8쌍의 UTP Cat % 를 사용
<br/>  
<br/>2.FDDI (Fiber Distributed Date InterFace) -ANSI에서 제안한 고속 LAN기술
<br/>광섬유 케이블을 사용하도록 설계된 이중 링 구조의 통신망 (토큰링)
<br/>단일 또는 다중 모드의 광섬유 케이블 모드를 지원
<br/>이중 링은 기본링과 보조링으로 구성되며, 동작방향은 서로 반대.
<br/>기본링은 모든 전송의 책임을 지며, 고장시 보조링을 통해 우회 전송
<br/>케이블 매체는 UTP, STRP, 광섬유 등을 이용
<br/>A,B CLASS로 분류하며 대역폭이 좁다
<br/>종류
<br/>FDDI-I : 광섬유 케이블을 매체로 100Mbps의 전송속도를 제공
<br/>FDDI - II : FDDI- I 기능을 확장한 것으로 1000Mbps의 전송속도를 제공
