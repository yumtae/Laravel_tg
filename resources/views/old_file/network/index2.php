<br/>Active Directory
<br/>시스템에서 원하는 사용자, 데이터베이스, 분산 구성요소, 리소스, 문서, 전자우편주소등의 개체를 조직적으로 관리하고 찾기 위한 도구로 사용
<br/>대규모 네트워크를 사용
<br/>DNS,TCP/IP 지원 및 LDAP와 같은 표준 프로토콜 지원을 포함하는 크로스 플랫폼 디렉토리서비스를 위한 개방형 표준 지원
<br/>도메인 콘트롤러의 이름과 위치를 사용하기 위해 DNS를 이용
<br/>Window환경에서 동일한 데이터베이스를 이용한 다양한 네트워크 서비스 제공
<br/>손쉬운 이행과 사용을 위해 표준 이름 형식 지원
<br/>풍부한 API 세트 제공
<br/>단순하고 계층적인 도메인 구조와 끌어서 놓기 방식의 간단하고 직관적인 관리
<br/>액티브 디렉토리에 의해 생성되는 구형의 카탈로그통한 신속하고 유연한 조회
<br/>
<br/>구성요소 : 도메인, 조직단위(OU), 도메인 트리, 포리스트, 글로벌 카탈로그, 도메인 컨트롤러, 사이트
<br/>
<br/>DNS(Domain Name Server)
<br/>특징
<br/>DHCP와 연동됨
<br/>주 영역 또는 보조 영역 관리
<br/>주 영역과 보조 영역이으로 나뉜다.
<br/>보조영역은 주영역으로부터 복제된 복사본을 가진다.
<br/>정방향 조회 - domain 주소를 ip주소로 변환하는 영역
