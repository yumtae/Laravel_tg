<br/>UDP 프로토콜
<br/>데이터 전송 전 연결설정 작업을 하지 않음.
<br/>데이터 전송속도의 향상을 꾀하기 위해 흐름제어, 오류정정 기능이 없음
<br/>빠른 수행속도를 필요로 하는 응용 프로그램과 멀티미디어 데이터 실시간 전송에 적합
<br/>신뢰성이 낮은 데이터 전송과 짧은 데이터 고속 전송에 유리
<br/>데이터 전송단위는 블록
<br/>송신측에서 무조건 송신만을 수행
<br/>전송 중 패킷이 사라지면 여부를 알수 없음.
