<br/><h3>4.정보전송방식</h3>
<br/>
<br/>에러종류와 원인
<br/>1.감쇠현상 
<br/>전송신호가 전송매체를 통과하는 과정에서 점점 신호세기가 약해짐
<br/>해결방법: 아날로그 전송시 증폭기사용, 디지털전송시 리피터 설치
<br/>
<br/>2.지연왜곡 
<br/>전송매체를 통한 신호전달이 주파수에 따라 그 속도를 달리함으로써 유발되는 신호손상
<br/>
<br/>3.잡음
<br/>열 잡음(백색잡음) -  가청 주파수의 모든 진동 스펙트럼을 고르게 포함한 잡음.열의 온도에 이의해 발생
<br/>충격잡음 - 전송시스템이 순간적으로 일어나는 높은 진폭의 잡음으로 주로 기계적인 충격, 낙뢰등의 자연현상
<br/>누화잡음 - 상호 인접한 전송매체의 전자기적 상호 유도 작용에 의해 발생하는 잡음
<br/>상호변조잡음 - 서로 다른 주파수들이 똑같은 전송 매체를 공유할 때 주파수들의 신호의 차이로 발생
<br/>
<br/>4.에코 
<br/>선로상에 임피던스의 변화가 있을 때 약해진 신호가 송신측으로 되돌아오는 것
<br/>
<br/>5.위상지터 
<br/>연속적으로 위상이 변하여 위상 사이의 편차가 커지는 것을 의미
<br/>
<br/>전송에러제어방식
<br/>1.반복전송방식 
<br/>송신측에서 동일한 데이터를 2번이상 연속하여 전송하고 수신측에서 이들 수신된 데이터를
<br/>비교하여 오류여부확인하는 방식
<br/>
<br/>2.궤환전송방식
<br/>전송된 데이터와 수신측을 경유하여 송신측으로 궤환된 데이터를 비교하여 검사하는 방식으로
<br/>전송하고자하는 데이터마다 2번이상의 전송이 필요하므로 전송요량의 낭비 발생
<br/>
<br/>
<br/>3.전진오류수정(FEC)방식 
<br/>정보 비트외에 오류의 검출 및 정정이 가능한 부가 코드를 함께하여 전송하여
<br/>수신측에서 이 부호를 이용하여 오류를 검출하고 자체적인 수정을 행하는 방식
<br/>★해밍코드, 순환잉여검사(CRC)등
<br/>
<br/>4.후진오류수정(BEC)
<br/>데이터 전송 과정에서 오류가 발생하면 수신측에서 재전송을 요구하는 방식
<br/>★자동반복(ARQ)등이 해당
<br/>
<br/>ARQ 종류
<br/>1.정지-대기 ARQ 
<br/>송신측이 하나의 블록을 전송한 후 수신측으로 부터 응답(ACK/NAK)을 기다리는 방식.
<br/>
<br/>2.연속적 ARQ 
<br/>GO-BACK N ARQ
<br/>여러 블록을 연속하여 전송하고, 수신츠긍로 부터 NAK신호를 받으면 에러가 발생한 블록 이후 모든 데이터를 재전송.
<br/>선택적 재전송 ARQ
<br/>여러 블록을 연속하여 전송시 수신측으로부터 NAK신호를 받으면 에러가 발생한 블록만 선택하여 재전송.
<br/>
<br/>3.적응적 ARQ
<br/>전송효율을 높이기 위해 블록의 길이를 동적으로 변경하여 전송하는 방식.
<br/>
<br/>에러 검출 방식
<br/>1.패리티 검사
<br/>전송되는 문자마다 1개의 패리티 비트를 추가하여 EVEN(우수) 또는 ODD(기수) 검사방법으로 오츄검출
<br/>
<br/>2.CRC(순환중복검사)
<br/>블록마다 검사용 코드를 부가시켜 전송하는 방식
<br/>데이터를 연속하여 전송하는 경우 집단 오류 검출을 위해 사용
<br/>주로 동기식 전송에서 많이 사용
<br/>
<br/>3.해밍코드 방식
<br/>에러를 검출하여 교정까지 할 수 있는 코드
<br/>자기 정정 부호의 하나로 비트 착오를 검출하여 1비트 착오를 정정하는 부호 방식
<br/>고속의 동기식 전송에서 사용
<br/>
<br/>다중화기
<br/>하나의 통신회선을 공유하여 여러개의 단말기가 컴퓨터에 접속할 수 있도록 하여 신호를 전송하고 이를 수신측에서
<br/>다시 몇개의 신호로 분리해주는 기기를 의미
<br/>
<br/>주파수분할 다중화방식(FDM) 
<br/>하나의 주파수 대역을 몇개의 대역폭으로 나누어 각 대역폭에 서로 다른 반송파를 실어 동시에 전송
<br/>채널간의 잡음막기위한 가드밴드가 필요, 모뎀이 필요없어 가격이 저렴하고 저속전송에 적합
<br/>
<br/>시분할 다중화방식(TDM) 
<br/>전송로의 데이터 전송을 위한 시간을 나누어 각 시간대에 서로 다른 신호를 전송(타임슬롯)
<br/>1.동기식 시분할 다중화방식 
<br/>각 터미널에 대해 전송할 내용이 있든 없든 동일한 타임슬롯을 할당(낭비발생)
<br/>
<br/>2.비동기식(지능형,통계적) 시분할 다중화방식 
<br/>전송할 내용이 있는 터미널에 대해서만 시간을 할당하여 전송 효율을 높이는 방식
<br/>
<br/>역다중화기
<br/>다중화기와 반대로 고속의 두개의 낮은 속도를 가진 선로로 변환하여 음성대역의 변복조기를 이용하여 
<br/>전송하고 수신측에서 다시 원래의 고속의 속도를 만들어냄
<br/>
<br/>집중화기 
<br/>여러개 채널을 몇개의 소수회선으로 공유시키는 방식
