<br/>무선인터넷 기술용어
<br/>IMT-2000 
<br/>이동통신의 문제점을 해결하기 위해 전세계 여러 나라간 협의를 거쳐 제정한 차세대 이동통신
<br/>사용 주파수 대여근 1,885MHZ ~ 2,200 MHZ로 향상된 기술과 고품질의 통신이 가능
<br/>
<br/>블루투스
<br/>근거리에서 디지털 기기간 무선 접속 지원하기 위한 통신기술로 개인통신망(PAN)을 위해 개발
<br/>1대의 마스터가 7대까지 연결하는 피코넷으로 구성
<br/>모바일, PDA, 프린터, 헤드셋등에서 무선으로 정보 전송하고 공유
<br/>
<br/>WAP(Wireless Application Protocol)
<br/>모바일 폰, PDA 등의 이동식 단말기에서 무선 인터넷에 직접 접속하기 위한 통신규약
<br/>유선망과 무선망 사이에서 프로토콜 변환을 위해 WML사용
<br/>무선으로 자료 전송하고 받을수있는 기술로 전송속도가 느린 휴대전화망의 단점을 고려하여 게이트웨이 방식을 채택
<br/>
<br/>VoIP(Voice over Internet Protocol)
<br/>인터넷을 이용하여 음성을 송수신하는 인터넷 전화 서비스
<br/>전송 중 손실된 패킷은 복원이 안되므로 통화품질이 좋지 않음
<br/>장거리 음성, 팩스 등 고품질의 PSTN 제공
<br/>
<br/>VPN(Virtual Private Network)
<br/>공중전화망에 사설망을 구축하여 통신사업자에게 전용회선을 임대
<br/>기존 사설망의 높은 비용부담을 해소
<br/>공중망을 통해 데이터를 전송하므로 송신측에서 암호화, 수신측에서 복호화 함
<br/>기능
<br/>터널링 - 복잡한 구간을 하나의 연결 경로로 표현
<br/>인증 - 터널링을 위한 인증과 데이터 무결성 인증으로 구분
<br/>암호화 - 데이터 변조나 스니핑 방지위한 암호화 제공
<br/>접근제어 - 데이터 출발지의 IP인증과 허가된 사용자여부에 따라 접근권한 부여
<br/>종류
<br/>IPSec-VPN,SSL-VPN
