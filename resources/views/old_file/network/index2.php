<br/>Expire : 보조영역에서 지정한 시간동안 Primary에 연결하지 못할 경우 해당 도메인이 유효하지 않다고 여기는 접속만료시간
<br/>Minimum : 다른 네임서버가 현재 ZONE정보를 가지고 갈 경우 그 정보에 대해 CACHE에 존 시간을 지정
