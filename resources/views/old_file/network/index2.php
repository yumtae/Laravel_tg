<br/><h3>1.네트워크 개요</h3>
<br/>
<br/>정보통신 (정보처리기능+정보전송기능)
<br/>개념
<br/>-컴퓨터에 의한 정보처리기술과 정보전송기능이 통합된 형태
<br/>-정보의 공유,변환,전송이 가능한 처리기술
<br/>
<br/>특징
<br/>-신속, 정확한 정보의전달.
<br/>-신뢰성이 높고 광대역 전송이 가능.
<br/>-정보 자원의 공유 및 이용.
<br/>-거리와 시간의 한계 극복.
<br/>-하드웨어 기술뿐만 아니라 소프트웨어 기술도 필요.
<br/>-에러제어방식이 요구.
<br/>
<br/>정보통신 3요소
<br/>-정보원, 전송매체, 정보처리원
<br/>
<br/>정보통신 시스템 기본구성요소
<br/>데이터 전송계: 단말장치(DTE), 데이터전송회선, 통신제어장치(CCU)
<br/>데이터전송회선 : 신호변환장치(DCE), 통신회선
<br/>데이터처리계: 컴퓨터(CPU,주변장치)
<br/>
<br/>단말장치(DTE)
<br/>-통신회선 양쪽 끝에 위치, 데이터 전송로에 적합한 신호나 데이터로 변환
<br/>
<br/>통신회선
<br/>-컴퓨터와 단말기간 또는 컴퓨터간에 상호간을 접속하는 통신로
<br/>
<br/>통신제어장치 
<br/>-데이터 전송계와 처리계의 접점에 위치하여 각종 제어기능과 데이터를 처리하기에
<br/>알맞은 형태로 변형
<br/>
<br/>네트워크 발전 단계
<br/>1단계:전화회선 이용한 음성회선
<br/>2단계:공중전화교환망(PSTN)-PC통신
<br/>3단계:고속고품질의 디지털 전용회선
<br/>4단계:광대역망을 이용한 데이터 전용 교환망
<br/>5단계:모든자료를 하나의 디지털 회선으로 통합 ISDN
<br/>
<br/>
<br/><h3>2.전송회선</h3>
<br/>
<br/>전송선로의 종류
<br/>유선
<br/>-나선/꼬임선
<br/>철선에 구리를 입힌 피복하지 않은 전설을 연결한선
<br/>기후변화에 따른 감쇠현상과 혼신현상이큼, 가격이싸고 전송속도와 거리에 제약
<br/>
<br/>-동축케이블
<br/>주파수에 따른 신호세력의 감쇄나 전송 지연의 변화가 적다, TV나 CATV 회선에 적합
<br/>
<br/>-광섬유케이블
<br/> 규소를 원료로 제작된 광섬유를 여러가닥 묶어서 만든 케이블
<br/> 전송신호를 레저광으로 하여 전반사에 의해 도파되는 원리를 이용한 통신선로 방식
<br/>
<br/>무선
<br/>-지상마이크로파(TV,인공위성) 
<br/>포물선 모양의 접시형 안테나인 마이크로웨이브파를 이용하여 정보전송
<br/>
<br/>-위성마이크로파
<br/>마이크로웨이브의 중계국인 통신위성을 이용하여 정보전송
<br/>
<br/>-라디오파 
<br/>라디오 웨이브를 이용한 정보전송
<br/>
<br/>광섬유 케이블의 구성요소
<br/>-발광기 - 정보의 송신에서 정보에 해당되는 빛을 발생
<br/>-수광기 - 빛을 받아들임
<br/>-광 케이블 - 광신호를 전송하는 선로
<br/>
<br/>광섬유 케이블의 특징
<br/>-잡음이나 누화가 없다
<br/>-광대역을 제공하여 데이터 전송율이 높다
<br/>-설치비용이 비싸고 접속이나 분기가 어려움
<br/>-보안성이 뛰어나다.
<br/>
<br/>
<br/>광통신
<br/>-레이저, 발광 다이오드 등에서 나오는 광파를 반송파로 이용하는 통신방식
<br/>-공간, 광섬유, 빔 등을 전송매체로 이용하며, 빛의 세기를 변조하거나 pcm변조 등을 통해 광대역 전송
<br/>-손실이 낮은 광섬유와 능률이 높고 수명이 긴 반도체 레이저 등이 개발됨에 따라 대용량 통신이 가능
<br/>
<br/>전송회선
<br/>-디지털 회선 
<br/>이산적인 변화를 갖는 신호를 전송하는 회선으로 0과 1의 신호만을 전송
<br/>PCM(펄스 부호 변조기) 회선 사용
<br/>
<br/>-아날로그회선 
<br/>사람의 목소리와 같이 연속적 변화를 갖는 신호를 전송
<br/>
<br/>전송방식 분류
<br/>단방향 방식(Simplex) - 라디오,TV
<br/>어느 한 쪽이 송신 또는 수신만 가능한 형태
<br/>
<br/>반이중 방식(Half Duplex) -FAX, 텔레스,무전기 
<br/>양방향에서 송수신 할 수 있으나 동시에는 불가능한 방식.
<br/>
<br/>전이중방식(Full Duplex) - 전화
<br/>동시에 양방향에서 송수신 할 수 있는 방식.
<br/>
<br/>통신 속도
<br/>-BPS(Bit Per Second) 
<br/>1초당 전송되는 비트 수를 의미
<br/>데이터 신호 속도 = 변조속도(baud) * 변조시 상태 변화 수
<br/>
<br/>-BAUD(보오) 
<br/>변조속도단위, 1초당 발생한 신호의 변화횟수
<br/>2비트-dibit, 3비트-tribit, 4비트-quadbit
<br/>
<br/>펄스당 4개의 비트를 전송할 수 있는 선로에서 초당 200개의 펄스를 전송할때 BPS,BAUD?
<br/>BPS = 4 * 200 = 800Bps ,BAUD= 200Baud (초당)
<br/>
<br/>
<br/>샤논의 정리
<br/>-통신용량 (잡음은 줄이고, 대역폭과 신호전력은 커야함)
<br/>단위시간에 전송회선이 최대로 전송할 수 있는 정보량
<br/>통신용량 = B*log2*(1+S/N)      
<br/>B:대역폭, S:신호전력, N:잡음
<br/>
<br/><h3>3.정보전송방식</h3>
<br/>
<br/>회선 접속방식에 의한 전송방식
<br/>-점 대 점 회선전송방식(POINT TO POINT)
<br/>두 장치 간의 전송매체가 직접 연결되어 있는 형태
<br/>데이터 전송에 책임을 지는 쪽 - 주국, 송신한 데이터를 수신하는 쪽 - 종국
<br/>
<br/>-멀티포인트 전송방식(MULTI POINT)
<br/>하나의 회선에 여러 단말기를 접속하는 방식
<br/>폴링 - 송신할 데이터가 있는지를 물어보는 것을 의미
<br/>셀렉션 - 데이터 수신할 수 있는지 물어보는 것을 의미
<br/>
<br/>회선제어절차
<br/>회선접속(물리적통신) - 데이터링크확립(논리적경로) - 메시지전송 - 데이터링크해제 - 회선접속해제
<br/>
<br/>직렬 및 병렬 전송방식
<br/>-직렬전송
<br/>하나의 문자를 구성하는 각 비트들이 하나의 전송선을 통하여 순서적으로 전송하는 방식
<br/>원거리 전송, 데이터 통신에서 사용
<br/>
<br/>-병렬전송
<br/>각 비트들이 여러 개의 전송선을 통하여 동시에 전송하는 방식
<br/>컴퓨터와 주변장치 사이의 데이터 전송방식에 사용
<br/>
<br/>
<br/>
<br/>
<br/>
<br/>
<br/>
<br/>동기/비동기 전송방식
<br/>-동기식 전송
<br/>정해진 블록단위로 데이터전송
<br/>전숑효율과 전송속도가 높음
<br/>단말기는 반드시 버퍼기억장치를 내장
<br/>정보의 프레임 구성에 따라 문자위주 동기방식과 비트 위주 동기방식으로 구성
<br/>프레임 구조가 125ms 단위로 구성
<br/>
<br/>-비동기식 전송
<br/>한 번에 한 글자씩 데이터 전송
<br/>스타트 비트와 스톱비트를 추가하여 한 번에 5~8비트씩 데이터 전송
<br/>각 문자와 문자 사이의 유휴시간이 일정하지 않음
<br/>전송효율이 나빠 단거리의 저속데이터 전송에 사용
<br/>
<br/>
<br/>start 
<br/>1 
<br/>1 
<br/>0
<br/>parity
<br/>stop 
<br/>
<br/>
<br/>데이터 전송방식
<br/>1.기저대역전송(baseband)
<br/>-디지털 형태로 표시되는 컴퓨터나 단말기의 출력신호를 변조하지 않고 그대로 전송하는 방식.
<br/>-근거리 통신에 적합하고 컴퓨터 주변기기간의 통신 등에 사용
<br/>
<br/>종류
<br/>-Rz(Return to Zero)
<br/>비트신호가 전송될 때 마다 상태가 변함.
<br/>0.5시간마다 +,- 상태 유지 후 0 상태로 복귀
<br/>
<br/>-NRZ(Non-Return to Zero) 
<br/>비트신호 시간만큼 +, 1상태를 유지
<br/>
<br/>-Bipolar(양극성) : 데이터, 이더넷, 토큰링
<br/>신호가 세개의 상태로 변하는 것으로 펄스의 유무나 극성으로 내용을 표현
<br/>기법이 단순하고 설치가 용이
<br/>
<br/>광대역 전송(broadband) : B-ISDN, 케이블TV 
<br/>하나의 전송매체에 여러채널의 데이터를 실어서 동시에 전송하는 방식.
<br/>하나의 전송매체로 음성, 데이터, 영상과 같은 멀티미디어 서비스를 제공할 수 있음.
<br/>변조작업을 거쳐 전송하므로 장거리 데이터 전송이 가능.
<br/>복잡한 기술과 설치시 비용이 비쌈
<br/>
<br/>신호변환 전송방식
<br/>1.아날로그 변조 : 아날로그 데이터 -> 아날로그 회선으로 전송
<br/>종류
<br/>진폭변조(AM)-변조파형에 따라 진폭을 변조
<br/>주파수변조(FM)-변조파형에 따라 주파수를 변조
<br/>위상편조(PM)-변조 파형에 따라 위상을 변조
<br/>
<br/>2.PCM(펄스코드변조방식) : 아날로그-> 디지털신호로 전송
<br/>입력 -> 표본화 -> 양자화 ->부호화 (순서만 잘나옴)
<br/>
<br/>3.디지털 변조방식 : 디지털데이터 -> 아날로그 신호
<br/>진폭 편이변조(ASK)
<br/>일정진폭의 반송신호 유무에 따라 2진 신호를 표현하는 방식의 모뎀
<br/>잡음등이 레벨변동에 약함
<br/>1200~2400bps의 속도
<br/>
<br/>주파수 편이변조(FSK) 
<br/>부호 0과 1에 각각 다른 주파수를 할당하는 방식
<br/>레벨변동에 영향을 받지 않고 고속전송에는 부적합
<br/>비동기식으로 2000bps이하의 속도
<br/>
<br/>
<br/>
<br/>
<br/>위상편이변조(PSK)
<br/>일정 주파수와 진폭의 정현파 위상을 2,3,8등분으로 나누어 각각 0 또는 1을 대응시키거나
<br/>2비트 혹은 3비트씩 한번에 할당하는 방식
<br/>2400~4800의 속도를 가짐. 
<br/>데이터 전송에 가장 적합한 동기방식
<br/>2위상 - 1회의 변조로 1BIT 사용 (2의1승)
<br/>4위상 - 1회의 변조로 2BIT 사용 (2의2승)
<br/>8위상 - 1회의 변조로 3BIT 사용 (2의3승)
<br/>
<br/>M=2의전송비트수 = LOG2M (M-위상편이변조)
<br/>
<br/>진폭위상편이변조(QAM,직교변조)
<br/>진폭편이와 위상편이변조를 혼합한 방식으로 반송파의 진폭 및 위상을 상호 변환하여 
<br/>신호를 싣는 변조방식
