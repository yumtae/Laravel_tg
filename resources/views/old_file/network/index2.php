<br/><h3>9.TCP/IP 프로토콜</h3>
<br/>
<br/>TCP/IP 프로토콜 개요
<br/>인터넷에서 가장 많이 사용되는 표준 프로토콜
<br/>OSI 계층의 전송계층과 네트워크 계층에서 동작하고, 상위의 응용프로그램을 구별한다.
<br/>
<br/>TCP
<br/>데이터의 전송 방법을 결정하는 프로토콜
<br/>OSI 7계층 중 전송계층에 해당하며 신뢰성 있는 연결형 서비스를 제공
<br/>패킷의 다중화, 순서제어, 오류 제어, 흐름 제어 기능을 제공
<br/>강력한 에러 제어기능이 있어서 정확한 데이터 전송
<br/>슬라이딩 윈도우 방식
<br/>패킷의 순차번호와 CRC 이용하여 신뢰성 있는 통신수행
<br/>프로토콜 : FTP, TELNET, HTTP, SMTP, POP
<br/>
<br/>IP
<br/>인터넷상에서 각 호스트의 주소를 결정하는 프로토콜
<br/>OSI 7계층의 네트워크 계층에 해당하며 데이터그램 기반으로 하는 비연결형 서비스
