<br/>3.ATM(Asnchronous Trasnsfer Mode) B-ISDN의 핵심기술
<br/>회선교환과 패킷 교환의 중간 형태로 정보를 고정 길이의 블록으로 분할하여 순차적으로 전송
<br/>데이터는 셀교환기법을 통해 동일한 고정길이의 셀로 변환시켜 전송(패킷교환방식에 비해 전송속도, 전송효율면에서 좋음) 
<br/>소량의 데이터 전송에는 부적합
