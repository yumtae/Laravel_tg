<br/>서브넷 마스크
<br/>Ip주소의 공간 낭비 문제를 해결하기 위해 서브넷 개념 이용
<br/>IP주소 부분에서 network부분과 host부분을 구분해주는 역할
<br/>호스트 이름으로붙 ip주소지에 대한 네트워크의 이름을 규정
<br/>패킷 전달과 대규모의 클래스를 나누거나 합하기 위해 존재
<br/>서브넷마스크 사용자가 ip주소를 사용할 때 지정해주는 것으로 왼쪽 bit부터 1로 시작
<br/>서브넷마스크 할당방법 - 맨 왼쪽 비트부터 연속적 1이표시된 부분까지 네트워크부분 그외 나머지 호스트 부분
