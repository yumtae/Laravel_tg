<br/><h3>5.프로토콜</h3>
<br/>
<br/>프로토콜
<br/>개념
<br/>컴퓨터와 컴퓨터, 정보통신망에서 원거리에 있는 통신 개체 사이의 정확한 데이터의 송수신을 위해 필요한 일련의 통신규칙
<br/>
<br/>기능
<br/>단편화 - 전송 블록을 같은 크기의 작은 블록으로 나누어 데이터 전송
<br/>재합성 - 단편화 된 데이터를 수신측에서 재구성하여 복원.
<br/>캡슐화 - 데이터에 제어정보를 추가하여 각 프로토콜에 적합한 데이터 블록이 되도록 함.
<br/>흐름제어 - 데이터의 양이나 통신속도 등 수신측의 처리능력을 초과하지 않도록 조정.
<br/>에러제어 - 오류나 착오등을 검출하고 정정.
<br/>순서제어 - 송신 데이터 순서대로 수신측에 전달.
<br/>연결제어 - 비연결 데이터전송, 가상회선을 위한 통신로의 개설, 유지, 종료.
<br/>주소지정 - 발생지, 목적지 등의 주소를 지정하여 정확한 송신을 하게함.
<br/>동기화 - 두 통신 개체간의 상태를 일치시킴.
<br/>다중화 - 하나의 통신로를 다수의 가입자들이 동시에 사용할 수 있도록 함.
<br/>
