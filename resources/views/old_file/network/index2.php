<br/>
<br/>무선 네트워크 기술
<br/>데이터 전송을 위해 전파나 적외선을 이용하여 네트워크를 구축하는 것
<br/>IEEE 802.11에서 표준화
<br/>유선 LAN에 비해 설치 비용과 시간이 절감
<br/>다른 기기에서 발생하는 잡음과 전파 방해의 영향을 받음
<br/>통신속도가 제한되고 구간 내에서는 노드가 이동하면서 통ㅇ신을 함
<br/>
<br/>전송기법
<br/>적외선 기법, 협대역마이크로 웨이브, 스펙트럼 확산대역
<br/>
