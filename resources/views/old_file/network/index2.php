<br/>기본 서브넷마스크
<br/>서브넷마스크는 ip주소와 달리 클래스 개념이 없고 앞에서부터 순차적으로 1씩 증가
<br/>IPv4의 서브넷마스크는 ip주소와 같이 32비트 2진수로 구성
<br/>ip주소만 있고 서브넷이 없는 경우 서브넷 마스크를 해당 주소대역의 기본으로 사용
