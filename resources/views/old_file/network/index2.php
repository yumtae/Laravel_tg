<br/>IP주소의 목적지 주소를 확인하여 해당 목적지까지의 최적의 경로를 지정
<br/>라우팅은 라우터 장비가 수행 즉, 라우팅 정보를 가진 라우터는 다른 라우터들과 주기적으로 교환함으로써 목적지까지 데이터 패킷을 전송
<br/>라우터는 모든 경로에 대한 정보를 라우팅 테이블에 저장,관리
<br/>라우팅이 가능한 프로토콜을 이용하여 호스트간 교신 수행
<br/>
<br/>정적 라우팅
<br/>목적지까지의 경로가 고정되어 있는 라우팅
<br/>관리자가 직접 수동으로 테이블 매핑 설정
<br/>소규모 네트워크에 적합, 네트워크의 변경이 크지 않은 환경에 적합
<br/>상호연결 네트워크가 적고, 트래픽 예측이 쉬운 환경에 적합
<br/>기존 경로에 장애 발생시 관리자가 직접 수동으로 재설정해줘야 하는 번거로움이 존재
<br/>
<br/>동적 라우팅
<br/>목적지까지 경로가 그때 그때 상황에 따라 변경되는 라우팅기법
<br/>트래픽이 변경되는 경우 자동으로 경로가 변경
<br/>망간의 최적의 경로는 routing Metric으로 결정
<br/>네트워크의 변화를 분석하여 라우팅 테이블 자동으로 업데이트(관리자의 설정작업 불필요)
<br/>경로의 장애 발생시 유연한 대처가 가능
<br/>초기 설정작업이 복잡하고, 라우팅 테이블이 서로 동일해야 함
<br/>라우팅 테이블의 유지관리를 위한 트래픽 증가
<br/>메모리 자원의 소모가 큼
<br/>프로토콜 : RIP, OSPF, BGP
<br/>
<br/>라우팅 프로토콜
<br/>패킷을 목적지까지 전송하기 위해 라우터 경로 설정하고 제어
<br/>거리벡터 알고리즘 방식 - 라우팅 테이블 유지할 때 목적지 거리를 라우터 수(Hop count)로 계산 
<br/>종류 : RIP, BGP
<br/>RIP(Round Information Protocol)
<br/>라우터 홉수에 따라 최단거리를 결정하는 프로토콜
<br/>홉수가 최대 16이므로 대규모 네트워크에는 부적합
<br/>라우터 정보 수명이 짧아서 외부라우터와 일정시간 이상 교신이 끊기면 라우팅 정보를 삭제
<br/>전체 라우팅 테이블을 가장 가까운 호스트에 매 30초마다 전송
<br/>
<br/>BGP(Border Gateway Protocol)
<br/>현재 인터넷에서 AS 사이에 사용되고 있는 대표적인 EGP 라우팅 프로토콜
<br/>TCP 포트를 이용하므로 신뢰할 수 있는 연결 지향적인 특징 가짐
<br/>거리 벡터 라우팅을 수행하며, 루프가 발생하지 않음.
<br/>AS가 다른 여러 AS와 연결되어 있는 경우에 사용
<br/>
<br/>AS(Autonomous System)
<br/>동일한 내부 라우팅과 보안정책을 사용하고 있는 망들의 집합을 의미
<br/>AS의 관리자는 자신의 AS에 대해 독자적으로 필요한 변경 및 유지보수 작업을 수행.
<br/>
<br/>링크상태 알고리즘방식 
<br/>목적지 거리를 라우터의 연결속도를 기준으로 계산
<br/>종류 : OSPF
<br/>네트워크에 변화가 있을때만 정보교환
<br/>멀티캐스트 기법
<br/>라우터 사이의 연결속도를 중심으로 라우팅
<br/>자신의 현재 상태를 알려줌.
<br/>자율시스템(AS)에서 사용하기 위해 설계되어, 라우터 끼리 그룹을 가져 그 그룹끼리만 라우팅 가능
<br/>사용자에 의한 경로 지정과 복수 경로 지정이 가능
<br/>라우팅 알고리즘이 복잡하여 라우터에 부담
<br/>VLSM 기술 지원하므로 IP주소 낭비 방지
