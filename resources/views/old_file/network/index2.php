<br/><h3>13.계정과 그룹 &파일시스템</h3>
<br/>
<br/>로컬 사용자 계정(Local User Account)
<br/>관리자가 임의로 등록하는 계정으로서 사용자는 계정이 존재하는 서버로만 로그온이 가능
<br/>일반적 계정은 모두 여기에 해당
<br/>허가된 서버 자원만 엑세스 가능
<br/>하나의 시스템에 로그인할 때 사용
<br/>계정 만료 기간을 두어 일정기간이 지나면 해당 계정을 사용하지 못하도록함
<br/>
<br/>내장 사용자 계정
<br/>Window 2000 설치시 관리자가 등록하지 않아도 미리등록되는 계정으로 삭제, 수정 허용안됨
<br/>시스템을 직접 이용하거나 계정정보가 시스템 설정과 연동되어 해당 계정의 권한이나 설정을 바꿈
<br/>
<br/>내장 그룹의 종류
<br/>Administrators
<br/>일반 사용자가 administrators 그룹에 구성원이 되면 관리자와 동등한 자격을 가짐
<br/>관리자 계정과 id가 달라서 관리자는 될 수 없지만, 실제 서버를 제어할 수 있는 권한은 가짐
<br/>권한 : 사용자 계정추가 및 삭제, 응용 프로그램 설치, 디스크 포맷
<br/>
<br/>Backup Operators
<br/>그룹구성원은 파일 사용 권한에 관계없이 컴퓨터 파일을 백업하거나 복원
<br/>권한 : 컴퓨터 로그온, 시스템 종료와 재부팅 수행
<br/>
<br/>Power users
<br/>그룹 구성원은 사용자 계정을 생성하거나 삭제할 수 있지만 계정 사용자는 자신의 계정에 대한 수정/삭제 권한만 가짐
<br/>관리자에게 계정관리를 위한 목적으로 권한을 위임할 때 사용
<br/>권한 : 장치 드라이버의 보안과 프린터 설치 등의 관리자 권한을 가짐.
<br/>
<br/>Users
<br/>그룹 구성원은 응용 프로그램, 로컬 네트워크, 네트워크 프린터를 사용하며, 로그아웃 및 화면 잠금 작업을 수행
<br/>※ 시스템 설정 변경은 하지 못함
<br/>권한 : 로컬 그룹생성, 자신이 만든 로컬 그룹을 삭제할 수 있는 권한
<br/>
<br/>
<br/>Guest
<br/>임시 사용자들에게 시스템의 제한적 기능을 사용하면서 로그온
<br/>일반 사용자를 geust 그룹에 등록하면 기본 사용과 종료는 가능하지만 다른 사용자들에 비해 제한 받음
<br/>
<br/>Everyone
<br/>시스템에 접근하는 모든 사용자 계정을 의미
<br/>
<br/>파일시스템
<br/>컴퓨터나 파일에 구별을 위한 이름을 붙이고, 저장이나 검색을 위해 그것들을 어디에 위치시킬 것인지를 결정.
<br/>모든 운영체제는 각각의 파일시스템을 사용하는데 서로 호환이 되지 않고 독특한 자신들만의 파일시스템을 만들어 운영
<br/>
<br/>윈도우 - FAT16,FAT32,NTFS
<br/>리눅스 - EXT2,EXT3,raiserFS
<br/>
<br/>NTFS(NT File System) 개념과 특징
<br/>NTFS는 Windows 버전의 기본 설정 파일 시스템
<br/>FAT32에서는 가능하지 않은 일부 디스크 관련 오류를 자동으로 복구.
<br/>대용량 디스크, 긴 파일 이름지원에 대한 기능 향상
<br/>사용 권한 및 암호화를 사용하여 승인된 사용자에게만 특정 파일을 액세스 할 수 있게해주므로 보안향상
<br/>디스크 결험 허용기능을 제공하여 다중 디스크 시스템에서 한 하드 디스크의 손실로부터 데이터를 보호
<br/>특정 파일이나 파일들이 있는 디렉토리를 자동적으로 압축할 수 있는 기능 지원
<br/>여러 개의 하드 디스크를 묶어 소프트웨어 적인 RAID를 구축
<br/>FAT에서 NTFS 파일시스템으로변환가능하지만 반대는 불가능
<br/>파일의 압축 저장과 실시간 복구 지원
<br/>파일 시스템 장애 발생시 FAT에 비해 복구가 어려움
<br/>
<br/>FAT(File Allocation Table) 개념과 특징
<br/>NT4에서 사용할 수 있는 가장 단순하고 최저 성능의 파일 시스템
<br/>다른 운영체제와의 호환성이 우수
<br/>단순한 구조와 높은 신뢰성, 많은 메모리를 필요하지 않음
<br/>저용량 볼륨에서 사용시 성능 우수
<br/>보안기능 취약
<br/>FAT에서 NTFS변환 : Convert 드라이브명 /fs:ntfs
