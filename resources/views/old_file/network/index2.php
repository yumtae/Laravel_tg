<br/><h3>15.DHCP &WINS</h3>
<br/>
<br/>DHCP
<br/>-관리자가 수동으로 IP주소를 입력하지 않고 모든 컴퓨터에 자동으로 IP주소를 전달하는 자동설정 방법 제공
<br/>네트워크 관리자의 많은 시간과 노력을 줄인다.
<br/>-서브넷마스크, 기본게이트웨이, DNS서버, WINS 서버 주소를 배분할 수 있으며 네트워크 구조에 따라 이들 모두 혹은 일부만 사용선택할 수 있음.
<br/>-IP주소 추적 가능
<br/>
<br/>WINS(Windows Internet Name Service)
<br/>NetBIOS 개념
<br/>-윈도우의 네트워크 상에서 컴퓨터 이름을 나타내기 위한 고유한 이름 값들을 부여하여 각자의 컴퓨터를 확인
<br/>-NetBIOS 이름은 16바이트를 사용. 이중 15바이트는 컴퓨터의 이름 마지막 1바이트는 이 컴퓨터가 어떤 서비스를 제공하는지 확인할 수 있는 접미사를 표현하는데 사용
<br/>-기본적으로 브로드캐스트를 사용하여 호스트와 통신하므로, 네트워크에 많은 트래픽을 줌
<br/>-NetBIOS 이름을 해석하기 위한 방법에는 LMHOST,WINS, HOST,DNS 등이 있음
<br/>HOST : 전체 도메인 이름(FQDN)을 이용하여 이름을 검색하는 파일
<br/>LMHOST - 다른 네트워크에 있는 NetBIOS를 사용하는 컴퓨터에 접속할 수 있다는 장점이 있지만, 각각의 컴퓨터에 Imhosts를 복사하여 가지고 있어야 한다는 단점이 존재, 또한 특정 컴퓨터의 IP변경시 모든 lmhosts를 업데이트 해주어야 하는 번거로움이 존재.
<br/>
<br/>WINDS의 개념과 특징
<br/>-네트워크에 있는 컴퓨터와 그룹에 대한 NetBIOS이름에 대응되는 ip주소를 찾아주는 서버
<br/>-Windows NT 서버의 일부로 컴퓨터 이름 정보를 저장하거나 조회할 수 있음.
<br/>-호스트 이름과 IP 주소 데이터베이스를 저장하는 독립된 서버를 네트워크에서 관리
<br/>-컴퓨터가 다른 장소로 이동되면 IP주소의 서브넷 부분이 변경됨.
<br/>-컴퓨터 이름과 IP주소 매핑을 위한 데이터를 테이블 내에서 자동 생성.
<br/>-호스트 이름을 TCP/IP 주소로 매핑하는 데이터베이스를 유지함으로써 TCP/IP의 모든장점을 이용하여 쉽게 다른 호스트와 통신할 수 있다.
<br/>-브로드캐스트 패킷을 가로채고 내부에서 처리하여 네트워크의 부하를 줄일 수 있다.
