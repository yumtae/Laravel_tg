<br/>IP(Internet Protocol)
<br/>네트워크를 통해 데이터를 전달하는 프로토콜
<br/>호스트의 인터넷 주소를 결정
<br/>IP 주소는 32bit 길이를 가지며, network ID, host ID로 구성
<br/>현재 사용하는 IP주소는 IPv4로 8비트씩 4옥텟으로 32Bit구성
<br/>동일한 네트워크 상의 모든 호스트는 같은 네트워크 id를 가진다.
<br/>
<br/>A클래스
<br/>범위 : 1.0.0.0 ~ 127.255.255.255
<br/>IP 시작비트 0 
<br/>국가나 대형 네트워크에서 사용
<br/>
<br/>B클래스
<br/>범위 : 128.0.0.0 ~ 191.255.255.255
<br/>IP 시작비트 10 
<br/>중대형 네트워크에서 사용
<br/>
<br/>C클래스
<br/>범위 : 192.0.0.0 ~ 223.255.255.255
<br/>IP 시작비트 110 
<br/>소형네트워크에서 사용
<br/>
<br/>D클래스
<br/>IP 시작비트 1110 
<br/>범위 : 224.0.0.0 ~ 230.255.255.255
<br/>IP 멀티캐스트 기능 수행을 위한 주소
<br/>멀티캐스트 - 네트워크 상에서 여러노드가 수신할 수 있는 특정 송출번지를 지닌 패킷
<br/>
<br/>E클래스
<br/>IP 시작비트 1111
<br/>실험용으로 사용
<br/>일반 PC에 직접설정 불가능
<br/>
<br/>
<br/>
 숫자로 이루어진 클래스 범위를 무작정 외우기에는 쉽지 않습니다. 그래서 쉬운 방법을 생각해 보았는데 의외로 정말 간단한 방법으로 외우게 되었습니다. 먼저 특징을 말씀드리자면 "주소체계 범위의 각 클래스 시작이 짝수이고 끝이 홀수"라는 것과, "범위가 반으로 나누어지게 된다는 것(n / 2)"입니다.<br/>
<ol>
	<li>IP에 입력하는 범위가 256개가 존재한다 (0 ~ 255)</li>
	<li>256부터 2씩 나눈다.</li>
	<li>256 / 2 = 128 - 1 = 127 (A Class 범위 : 0 ~ 127)</li>
	<li>128 / 2 = 64 + 127 = 191 (B Class 범위 : 128 ~ 191)</li>
	<li>64 / 2 = 32 + 191 = 223 (C Class 범위 : 192 ~ 223)</li>
	<li>남은 32의 수는 16씩 나누어서 D Class와 E Class가 가진다.</li>
	
</ol>
 또 다른 방법으로는 첫 번째 bit를 알면 됩니다. B클래스를 보신다면 Network ID와 Host ID 둘 다 16bit(2byte) 씩 나눠서 가지고 있습니다. 그런데 할당 가능한 수가 Network ID는 16,384개이고 Host ID는 65,534개로 차이가 납니다. 이는 위에서 잠깐 언급되었는데 각 클래스를 구분하기 위해서 bit를 사용하기 때문입니다. A클래스는 0으로 시작하고, B클래스는 10으로 시작하고, C클래스는 110으로 시작하기에 사용되는 비트만큼 Network ID로 할당 가능한 수가 줄어들게 됩니다.


