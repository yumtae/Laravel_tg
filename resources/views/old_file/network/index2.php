<br/><h3>6.통신망</h3>
<br/>
<br/>통신망
<br/>둘 이상의 지점간의 의사 또는 정보를 전달하기 위한 신호변환, 선택수단, 전송수단등의 구성요소로 이루어진 종합적인 체계를 의미
<br/>
<br/>토폴로지 
<br/>정보전달하는 매체를 통해 네트워크가 배열되어 있는 형태
<br/>토폴로지 종류
<br/>트리형 - 분산 처리 시스템을 구성하며 통신선로가 짧다
<br/>링형 - 양쪽 방햑으로 접근가능하여 통신회선 장애에 대해서 유연, 근거리 통신망에서 사용
<br/>스타형 - 중앙에 호스트 컴퓨터를 중심으로 터미널들이 연결되어 있는 중앙집중형, 중앙 컴퓨터 오류 발생시 전체 시스템 마비
<br/>망형 - 모든 단말기와 단말기들을 통신회선으로 연결시킨 구조로 공중데이터 통신망에서 주로사용, 노드의 연결성 높음
<br/>버스형 - 하나의 통신회선에 여러대의 단말기들을 접속하여 연결한 구조, 물리적구조가 단순하고 단말기 추가삭제가 용이, 회선에 이상이 생기면 전체 시스템 마비
<br/>
<br/>교환망 : 여러 개 통신회선의 중간에서 각각의 노드들이 전송하는 데이터를 원하는 곳으로 전송해주는 기법
<br/>회선 교환망
<br/>컴퓨터와 단말기 혹은 컴퓨터간의 데이터 교환이 아닌 회선자체를 교환
<br/>대량의 정보를 송수신할 수 있는 팩시밀리 전송
<br/>통신경로가 설정되면 데이터의 형태, 부호, 전송제어 절차 등에 대한 제약을 받지않는다
<br/>종류 : 시분할 교환방식, 공간분할 교환방식
<br/>
<br/>메시지 교환망
<br/>전송되는 데이터를 일련의 메시지 단위로 교환
<br/>교환기가 송신측 컴퓨터의 메시지를 받아 축적하였다가 수신측 컴퓨터가 수신가능한 상태가 되면 정보전송
<br/>각 메시지마다 수신 주소를 붙여 전송하며, 전송 경로가 다르다.
<br/>응답시간이 느려 대화형 데이터 전송에는 부적합
<br/>
<br/>축적방식 - 패킷 교환망
<br/>메시지를 일정 길이의 전송단위로 작게 나눈 패킷단위로 전송하는 방식
<br/>패킷 - 메시지를 정해진 크기의 비트 수로 나눈 후 정해진 형식에 맞춰 만들어진 데이터 블록
<br/>모든 사용자 간에 빠른 응답시간을 제공하기 위해 사용
<br/>음성 전송보다 데이터 전송에 더 적합하며 전송량 제어와 전송 속도 변환이 가능.
<br/>전송 실패시 재전송 가능
<br/>대량의 패킷 전송시 전송지연이 발생
<br/>종류 : 가상회선, 데이터그램 방식
<br/>
<br/>데이터그램 방식 
<br/>패킷 전송시 특정 경로 설정 없이 노드들의 트래픽 상황을 감안하여 전송되는 방식으로 각 패킷마다 목적지로 가기 위한 경로배정이 독립적으로 이루어짐.
<br/>적은양의 데이터 전송할 때 유리
<br/>
<br/>가상회선방식 
<br/>패킷 전송 되기전에 각 단말기간에 논리적인 가상회선이 확정되어 패킷들이 순차적으로 전송되는 방식으로 여러 사용자가 하나의 통신회선을 공유할 수 있으므로 회선 이용률이 높다
<br/>특징 : 데이터 전송의 안정성, 신뢰성보장, 송수신 순거가 같음.
<br/>
<br/>근거리통신망(LAN)
<br/>일정지역 내의 설치된 통신망으로 각종 기기 사이의 통신을 실행
<br/>근거리에서 고속통신이 가능, 경로설정이 불필요, 확장성과 재배치가 용이, 오류발생율이 낮음
<br/>
<br/>LAN 통신장비 종류
<br/>라우터
<br/>분리된 네트워크를 연결하는 장치로 네트워크 계층 간의 최적의 경로를 설정
<br/>브리지
<br/>같은 LAN 프로토콜을 지닌 두 LAN을 연결하는 것으로 물리계층과 데이터링크계층 연결
<br/>허브 
<br/>가까운 거리의 컴퓨터들을 연결하는 장비
<br/>리피터
<br/>감쇠된 신호를 증폭하여 네트워크 길이를 확장하여 깨끗한 신호를 수신할 수 있도록함 -> 물리계층
<br/>스위치
<br/>패킷이 전달되어야 할 경로를 제공해주는 역할 담당
<br/>게이트웨이
<br/>완전히 다른 프로토콜 구조를 갖는 네트워크를 연결하여 데이터 전송하도록 함.
<br/>
<br/>CSMA/CD방식
<br/>데이터 충돌을 막기위해 송신 데이터가 없을 때에만 데이터를 송신하고, 전송 도중 충돌이 감지되면 전송을 멈추고, 충돌을 알리는 잼 신호를 전송후 일정 시간이 흐른후 다시 재전송하는 방식
<br/>특징
<br/>통신량이 적을때는 채널 이용률이 높음.
<br/>어느 한 터미널이 고장나더라도 다른 터미널의 통신에 전혀 영향을 미치지 않음.
<br/>모든 제어기는 동등한 엑세스 권한이 있음.
<br/>회선상 데이터 없는 확인-> 데이터전송 -> 충돌발생(동시에 데이터 전송시 발생) ->회선 감지
<br/>이더넷 
<br/>CSMA/CD방식을 사용하는 LAN
<br/>100MBPS이상을 고속 이더넷(fast ethernet)
<br/>10 BAST T - 전송속도가 10MBPS
<br/>BASE - 베이스밴드 전송방식
<br/>T - 전송매체로 꼬임선 이용
<br/>10 BASE F -전송매체로 광케이블 사용
<br/>
<br/>토큰패싱 방식
<br/>전송매쳉 접근하기 위해 토큰이라는 특정 패킷사용
<br/>통신회선에 대한 제어신호가 각노드들을 순차적으로 옮겨가면서 데이터를 전송하는 방식
<br/>토큰링
<br/>모든 노드사이에 물리적인 링이 연결되어 토큰이 링을 따라 순차적으로 전송
<br/>
<br/>토큰버스
<br/>모든 노드 사이에 버스형 토폴로지 이용, 토큰링과 구조는 같고 케이블의 양 끝에 신호의 바인딩 막기위한 터미네이터 장치 사용
<br/>
<br/>LAN표준
<br/>IEEE 802.2 LLC
<br/>IEEE 802.3 CSMA/CD방식
<br/>IEEE 802.4 TOKEN버스 방식
