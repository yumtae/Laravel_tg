<h3 class="sosik" style="box-sizing: border-box; font-size: 16px; padding: 7px 10px 5px; border-right-width: 0px; word-spacing: 3px; text-align: left; border-left: #666 10px solid; background-color: #fafafa; color: #333; margin: 0 0 10px; letter-spacing: 1px; line-height: 1.5; font-weight: bold; border-top-width: 0px; border-image: initial;">소켓 아이템 벤더 레시피</h3>
<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;">&nbsp;초반부터 중후반까지 꾸준히 하는 파밍의 기초 부분입니다. 크래프팅 및 소켓 작업에 많이 사용되는 오브를 모으기 위한 것으로 일명 신호등 아이템(최소 빨, 파, 녹 소켓이 연결되어 있는 아이템)을 팔아 크로마틱 오브를, 6 소켓 아이템을 팔아 주얼러 오브 7개를, 6 링크 아이템을 팔아 디바인 오브를 획득할 수 있습니다. 가장 많이 작업하게 될 파밍 작업 중 하나입니다.</p>
