<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;">&nbsp;패스오브엑자일에는 돈의 개념이 없습니다. 대신에 오브(Orb)라는 것이 존재하며 여러가지 오브를 물물교환식으로 상점에서 판매 혹은 유저와 거래하면서 구할 수 있습니다. 그 중 화폐의 가치처럼 쓰이는 것이 카오스 오브(Chaos Orb)와 엑자일 오브(Exalted Orb)인데 카오스 오브의 경우 특정 벤더 레시피를 구해 구할 수 있고 그 외의 유용한 오브들도 상점에서 판매하는 방법에 따라 구할 수 있는 방법이 있습니다.&nbsp;</p>
<p style="text-align: justify;">&nbsp;</p>
<h3 class="sosik" style="box-sizing: border-box; font-size: 16px; padding: 7px 10px 5px; border-right-width: 0px; word-spacing: 3px; text-align: left; border-left: #666 10px solid; background-color: #fafafa; color: #333; margin: 0 0 10px; letter-spacing: 1px; line-height: 1.5; font-weight: bold; border-top-width: 0px; border-image: initial;">상점의 고정 교환비를 통한 구매</h3>
<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;">&nbsp;1~10장까지 메인 시나리오를 진행하면서 상점을 통해 요구하는 아이템과 교환할 수 있습니다. 오브마다 고정으로 정해진 비용이 있어 그에 맞게 교환하면 되지만 일반적으로 리그안에서 유저들과 거래하는 것보다는 조금 비싼 편입니다. 다만 유저와 거래하기 귀찮거나 시간 소모를 하기 싫을 때는 가장 빠르게 필요한 오브를 획득할 수 있는 장점이 있습니다.&nbsp;</p>
