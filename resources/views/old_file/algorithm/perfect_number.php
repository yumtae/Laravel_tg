<script type="text/javascript">


var LM = 0; //완전수
var N; // 임의의정수
var J; //약수를 담을 변수
var R;  // 임의의정수를 N/2 이하의 수로 나눈 나머지
var K; // 어떤 수의 최대의 약수
 // 약수의 합




for(N = 4 ; N < 8129 ; N++){
	
	SUM = 0; //약수의 합으로 완벽수와 비교하기위한 변수

	K = Math.floor(N / 2); //소수점 버림
	
	for(J=1 ; J <= K ; J++ ){
		R = N % J;
		
		if(R == 0){
			SUM = SUM + J;
		}
	
	}

	if(N == SUM){
		
		LM = LM+1;
		document.write(LM + '번째 완전수 ' + N +  '<br/>');
	}

}

document.write('<br/> 총 갯수 : ' + LM);
