<br/>문제에서 '4부터 1000까지의 정수 중 이러한 약수를 갖는 수' 라고 하였는데, 
순서도 첫 반복문에서  'N = 4, 1000, 1' 와 같이 한 것을 볼 때, N 이 완전수를 담는 변수로 쓰였음을 짐작할 수 있다.
<br/>
<br/>문제의 (조건)에서 '최대의 약수는 어떤 수를 2로 나눈 값보다 같거나 작다' 라고 하였는데,
<br/>K = INT(N/2) 를 보면, K 라는 변수를 최대의 약수로 사용한 것을 짐작할 수 있다.
<br/>여기서, INT(  ) 처리를 한 이유는 홀수의 경우를 고려하기 위해서 이다.
<br/>
<br/>약수는 1 ~  K  사이의 수들 중 N을 그 수로 나누었을 때 나머지가 0 이어야 약수임을 증명할 수 있다.
<br/>R = MOD(N,J) 를 보면, N 을 J 로 나눈 나머지를 R에 저장하였기 때문에, R은 나머지 값을 저장하는 변수이고
<br/>J 는 1 ~ K 사이의 약수를 구분지어주기 위한 변수임을 알 수 있다. 
<br/>
<br/>결국, 
<br/>N 에 J 를 1부터 K 까지 1씩 증가시키면서 나누었을 때, 나머지 R이 0 이면, J 는 N의 약수가 되는 것이다
<br/>그리고 SUM 은 약수의 합으로 R = 0일 때, J 를 누적시킨 합계이다.
<br/>
<br/>
<br/>① J
<br/>② R
<br/>③ SUM = SUM + J
<br/>
<br/>
<br/>이 때, 약수의 합이 어떤 정수와 같으면 그 정수는 완전수이기 때문에, SUM 이 N 과 같으면 위 조건을 만족한다.
<br/>이 때 문제에서 N 을 출력하고 그 개수를 출력하라고 하였다.
<br/>
<br/>LM 은 N 완전수의 개수를 카운트하기 위한 변수로서, 완전수 조건이 만족될 때마다 1씩 증가시켜주면 된다.


<br/>④ SUM = SUM + J
