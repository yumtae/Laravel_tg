<script type="text/javascript">

var T = new Array(17,77,11,66,1,14,13,16,19,20); //배열  
var N; //배열첨자  
var M = 100; //최소값 
var C; //10과의 차  
var S; //차가 최소인 자연수 
	


for (N = 0 ; N < T.length ; N++){
	

	if(T[N] >= 10 ){
		C = T[N] - 10;
	}else{
		C= 10 - T[N]
	}

	if(M > C){
		M = C;
		S = T[N];
	}
	
	
}
	document.write(S + '<br/>');


</script>

