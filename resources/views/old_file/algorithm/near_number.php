<br/> ▶알고리즘 원리 이해
<br/>
<br/>배열에 기억된 값을 10과 비교하여 10과 차이를 구한 후 차이가 가장 작은 값에 해당하는 배열에 기억된 자연수를 최종 출력하는 알고리즘입니다.
<br/>
<br/>곧, 최소값을 구하는 형태의 알고리즘으로 실제 출력되는 값은 10과의 차에 대한 최소값으로 결정된 원래의 수를 출력하는 알고리즘입니다
<br/>
<br/>​▶순서도 수행 순서 이해
<br/>
<br/>1. 배열의 자연수와 10을 비교하여 배열의 자연수가 10보다 크거나 같으면 배열의 자연수에서 10을 뺀 값을 C에 기억시키고 만약 10이 작다면 10에서 배열에 입력된 자연수를 뺀 값을 C에 기억시킵니다.
<br/>
<br/>2. 최소값 변수 M과 C를 비교하여 C가 작다면 최소값 변수 M에 C를 기억시킨 후 배열에 기억된 자연수를 S에 기억시킵니다.
<br/>
<br/>3. 위의 순서를 10회 반복수행 한 후 최종적으로 S를 출력합니다