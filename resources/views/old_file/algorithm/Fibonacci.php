<br/>
<br/><필요한 변수>
<br/>
<br/>n-2 번째를 가리킬 변수  ( 초항 1로 초기화)              --->  a
<br/>
<br/>n-1 번째를 가리킬 변수  (초항 1로 초기화)               --->  b
<br/>
<br/>n 번째를 가리킬 변수                                                --->  c
<br/>
<br/>​
<br/>
<br/>​<img src='./img/Fibonacci4.png'>
<br/>
<br/>
<br/>​
<br/>
<br/>​
<br/>
<br/>초기화한 a = 1, b = 1 이 의미하는 것은
<br/>
<br/>피보나치 수열의 규칙인 처음 두 항이 1이라는 것입니다
<br/>
<br/>그리고 3번째 항은 1번째 항의 값과 2번째 항의 값의 합이므로
<br/>
<br/>c 라는 변수를 그 다음 값을 가리키는 변수로 사용해서 
<br/>
<br/> c = a + b         로 연산을 해주면 다음 값을 만들 수 있습니다
<br/>
<br/>​
<br/>
<br/>​
<br/>
<br/>①  :   1
<br/>
<br/>②  :   c = b + a
<br/>
<br/>​
<br/>
<br/>​
<br/>
<br/>​
<br/>
<br/>다음으로 4번째 항을 만들어내기 위해서는
<br/>
<br/>2번째 항과 3번째 항의 합을 구해야합니다
<br/>
<br/>현재 a 라는 변수는 1번째 항을
<br/>
<br/>b 라는 변수는 2번째 항을
<br/>
<br/>c라는 변수는 3번째 항의 가리키고 있습니다
<br/>
<br/>​
<br/>
<br/>각각의 변수가 바로 다음 순서의 자리를 가리키면
<br/>
<br/>a (1번째 --> 2번째)
<br/>
<br/>b (2번째 --> 3번째)
<br/>
<br/>c (3번째 --> 4번째)
<br/>
<br/>변수 c로 4번째, 항을 만들어낼 수 있습니다
<br/>
<br/>​
<br/>
<br/>​
<br/>
<br/>a = b,          a가 b의 값을 담으면 2번째를 가리키는 것으로 간주할 수 있고
<br/>
<br/>b = c,          b가 c의 값을 담으면 3번째를 가리키는 것으로 간주할 수 있습니다
<br/>
<br/>​
<br/>
<br/>문제에서 출력하고자하는 것은 N항의 값이 20보다 작을 때까지,
<br/>
<br/>피보나치 수열의 합계와 갯수입니다
<br/>
