<br/>
<br/>다음 피보나치 수열에 대하여  20보다 작은 항까지의 개수와 합을 구하는 알고리즘을 제시하라.
<br/>
<br/>​
<br/>
<br/>1 + 1 + 2 + 3 + 5 + 8 + 13 …
<br/>
<br/>​
<br/>
<br/>​
<br/>
<br/>​
<br/>
