<h2>데이터 통신</h2>


<p><b>VPN / 가상 시설 통신망</b> - 가상 사설 네트워크로서 인터넷 등 통신 사업자의 공중 네트워크를 사용자가 마치 자신의 전용 회선처럼 사용할 수 있게 하는 서비스</p>

<p><b>WLAN / 무선랜 </b> - 무선 접속 장치가 설치된 곳을 중심으로 무선 주파수 기술을 이용해 근거리 <b>WLAN</b> 카드가 장착된 개인 휴대 정보 단말기를 이용해 인터넷에 연결하는 네트워크</p>

<p><b>OFDM(Orthogonal Frequency Division Multiplexing) / 직교 주파수 분할 다중</b> - 고속의 데이터를 각 반송파가 직교 관계에 있는 다수의 부반송파에 나누어 실어 다중 전송하는 디지털 변조 방식</p>

<p><b>FTP / 파일 전송 프로토콜</b> - 컴퓨터와 컴퓨터 또는 컴퓨터와 인터넷 사이 파일을 주고받을 수 있도록 하는 원격 파일 전송 프로토콜</p>

<p><b>다중 경로 페이딩 </b> - 신호를 전달하는 전파가 통로상의 여러 가지 장애물에 의해 두 개 이상이 경로를 통하여 수신측에 도착할 경우 , 그합성 신호가 시공간적으로 강도가 변하는 현상</p>

<p><b>NDN(Named Data Networking)</b> - 인터넷에서 데이터 전송을 콘텐츠 자체의 정보와 라우터 기능만으로 수행하는 기술</p>

<p><b>원격검침 </b> - 전기,가스,수도 등 검침원이 일일이 고객을 방문하지않고, 원격에서 단말기를 이융해 검침데이터를 읽을 수 있는 시스템</p>

<p><b>NGN / 차세대 통신망 </b> - ITU-T에서 개발하는 유성망 기반의 차세대 통신망</p>

<p><b>GLONASS </b> - 미국의 GPS , 유럽의 갈릴레오 위치결정시스템과 유사한 러시아의 전파 위성 항법 시스템</p>

<p><b>MICS / 무선의료기기 </b> - WBAN의 의료분야에서 임플란트 장치 간의  통신 서비스를 부르는 명칭</p>

<p><b>SDN / 소프트웨어 정의 네트워크 </b> - 네트워크를 컴퓨터처럼 모델링하여 여러 사용자가 각각의 소프트웨어 프로그램들로 네트워킹을 가성화 해 제어하고 관리하는 네트워크</p>

<p><b>NFC / 근거리 무선 통신 </b> - 고주파를 이용한 근거리 무선통신</p>

<p><b>SSID / 서비스 세트 식별자 </b> - 무선랜을 통해 전송되는 모든 패킷의 헤더에 존재하는 고유 식별자(서로다른 랜을 구분하기위해사용)</p>

<p><b>Qos / 서비스품질 </b> - 서비스의 질을 의미하는 것으로, 네트워크에서 일정 기준 이하의 지연 시간이나 데이터 손실률 등을 보증하기 위한 서비스 규격</p>

<p><b>MODEM / 모뎀 </b> - 디지털신호를 아날로그신호로 변환하는 변조과정과 아날로그를 디지털신호로 복조과정을 수행하는 신호변환장치</p>

<p><b>TEIN  / 트랜스 유라시아 네트워크</b> - 아시아와 유럽을 연결하는 초고속 정보 통신망</p>

<p><b>Telepresence / 원격 현장감 </b> - 공간적으로 떨어져있는 장소 또는 가상의 장소를 신체적으로 경험하는 것</p>

<p><b>Carrier Aggregation  / 반송파 묶음 </b> - 복수의 주파수 대역을 동시에 사용할 수 있는 대역폭 확장기술</p>

<p><b>NMRI / 네트워크 준비 지수 </b> - 세계 경제 포럼이 국제적인 경영대학인 인시아드와 공동으로 개인과 정부, 기업의 정보통신기술 발전도와 경재력을 국가별로 평가한 지수</p>

<p><b>MAN / 도시권 통신망 </b> - LAN과 WAN의 중간 형태로 LAN의 기능을 충분히 수용하면서 도시 전역 또는 도시와 도시 등 넓은 지역을 연결한는 통신망으로, 한 도시 내의 네트워크들을 하나의 거대한 네트워크로 서로 연결하는데 사용</p>

<p><b>Video On Demand / 주문형 비디오 </b> - 다양한 정보의 데이터베이스를 구축해 연결된 컴퓨터 또는 TV를 통해 사용자가 요구하는 정보를 원하는 시간에 볼 수 있도록 전송하는 멀티미디어 서비스</p>

<p><b>SYN / 동기 상태 부호 </b> - 동기 신호 방식에서 다른 문자를 보내지 않는 유휴상태에서 데이터 단말 상호 간의 동기를 취하거나 , 동기를 유지하기 위한 신호로 사용되는 전송 제어 문자</p>

<p><b>TCP/IP </b> - 인터넷 연결된 서로 다른 기종의 컴퓨터들 간에 데이터를 주고받을 수 있도록 하는 표준 프로토콜</p>

<p><b>프로토콜 </b> - 네트워크에서 서로 다른 컴퓨터들 간에 정보 교환을 할 수 있게 해주는 표준화된 통신 규약</p>

<p><b>UWB(Ultra WideBand) / 초광대역  </b> - 짧은 거리에서 많은 양의 디지털를 낮은 전력으로 전송하기 위한 무선 기술로 무선 디지털 펄스 라고도 한다</p>

<p><b>피코넷 </b> - 여러 개의 독립된 통신장치가 블루투스 기술이나 UWB 통신기술을 사용하여 통신망을 형성하는 무선 네트워크 기술이다</p>

<p><b>DPM / 디지털 우편소인 </b> - 만국우편연합에서 표준화한 온라인 자료에 대한 전자적인 우편배달 서비스</p>

<p><b>고속 하향 패킷접속 </b> - 비동기식 3세대 이동 통신의 하향 링크에서 10Mbps 수준의 고속 패킷 데이터 서비스를 제공하는 전송 규격</p>

<p><b>U-Health / 유헬스 </b> - 물리적 공간과 네트워크로 연결된 첨단 보건의료 기술의 전자적 공간을 연결하여 보건 의료 대상자의 삶과 진료가 중심이 되도록 하는 것을 의미</p>

<p><b>디저라티 </b> - 디지털과 리터라티의 합성어로 컴퓨터, 종보통신 등 디지털 분야의 지식이 많은 사람을 지칭하는 용어</p>

<p><b>WBAN(Wireless body area network) </b> - 웨어라블 또는 몸에 심는 형태의 센터나 기기를 무선으로 연결하는 개인 영역 네트워킹 기술</p>

<p><b>CTTH (Coax To the home) </b> - 기존 케이블방송망으로 초고속/대용량 서비스를 제공하는 새로운 전송 방식</p>

<p><b>포스트넷 </b> - IT 기반 우편물류 통합정보시스템</p>

<p><b>지그비(ZigBee) </b> - 저속 전송속도를 갖는 홈오토메이션 및 데이터 네트워크를 위한 표준 기술</p>

<p><b>디지털 접근 지수 </b> - 정보통신기술의 접근성과 서비스 이용에 관한 전 세계적 비교지수</p>

<p><b>차량 멀티홈 통신 </b> - 자동차에 IT기술을 접목해 차량 충돌을 예방하는 기술</p>

<p><b>LAN (Local Araa Network) / 근거리통신망</b> - 자원공유를 목적으로 회사,학교,연구소 등의 구내에서 사용하는 통신망으로서, 직원들이 가장효과적으로 공동사용할 수 있도록 연결된 통신망이다. 전송 거리가 짧아 고속 전송이 가능하며, 차량 간 통신으로 충돌을 예방하는 기술이다</p>

<p><b>망중립성 </b> - 모든 네트워크 사업자는 모든 콘텐츠를 동등하게 취급하고 어떠한 차별도 하지 않아야 한다는 원칙, 중립성을 보장하기 위해 비차별,상호접속,접근성 등 3가지 원칭이 모든 통신망에 동일하게 적용 되어야한다.</p>

<p><b>GIS / 지리정보시스템 </b> - 지리적으로 자료를 수집,정보,분석,출력할 수 있는 컴퓨터 응용시스템으로, 위성을 이용해 모든 사물의 위치 정보를 제공해 주는 것</p>

<p><b>IT839 전략 </b> - 미래의 정보 기술 시장을 선도하기 위하여 정보통신부가 2004년 2월 수립한 IT 성장 전략</p>

<p><b>USN / 유비쿼터스 센서 네트워크 </b> - 각종 센서로 수집한 정보를 무선으로 수집할 수 있도록 구성한 네트워크. 즉, 필요한 곳에 RFID 태그를 부착하고, 이를 통하여 사물의 인식 정보는 물론 부녀의 환경정보까지 탐지하여 이를 네트워크에 연결 하여 정보를 관리하는 것</p>

<p><b>ANT+protocol </b> - 2.457MHz 대역에서 동작하는 초저전력 무서 센서 네트워크 프로토콜로 상호 운용성을 보장한다</p>

<p><b>레드섹 프로토콜 </b> - RADIUS 데이터를 전송제어 프로토콜이나 전송계층 보안을 이용하여 전송하기 위한 프로토콜이다,Radius over TLS 의 준말로 이용자가 접속을 요구할 때 이용자의 ID나 암호와 같은 정보를 서버로 보내어 식별하고 인증을 수행한다.</p>


