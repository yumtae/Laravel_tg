<h2>인터넷</h2>



<p><b>WebKit / 웹키트</b> - 웹 브라우저를 만드는데 기반을 제공하는 레이아웃 엔진</p>

<p><b>MashUp / 매시업 </b> - 웹에서 제공하는 정보 및 서비스를 이용하여 새로운 소프트웨어나 서비스, 데이터베이스 등을 만드는 기술</p>

<p><b>VoIP </b> - 인터넷 프로토콜 상의 음성 전송이란 의미로서, 인터넷을 통하여 통화 할 수 있는 기술</p>

<p><b>URL </b> - 인터넷 상에 존재하는 각종 자원이 있는 위치를 나타내는 표준 주소 체계</p>

<p><b>쿠키 </b> - 웹사이트에서 사용자의 하드에 저장하는 특별한 텍스트파일로서, 인터넷 사용자에 대한 특정 웹 사이트의 접속 정보를 저장하기 이한 것</p>

<p><b>IMS </b> - 인터넷프로토콜 기반으로서 음성,오디오,비디오 및 데이터 등의 멀티미디어 서비스를 제공하는 규격</p>

<p><b>멀티캐스트 </b> - 구내 정보 통신망이나 인터넷에 접속되어 있는 일부 사용자 내에서 한사람이 몇사람에게 정보를 송신하고 그것을 수신한 몇 사람이 같은 내용을 버킷 릴레이식으로 복수의 사람에게 송신함으로써 정보를 전파하는 특정 다수인에 대한 전송</p>

<p><b>비트코인 </b> - 가상의 디지털 화폐로, 2009년에 만들어졌으나 개발자는 분명치 않다.</p>

<p><b>RIA / 리치 인터넷 애플리케이션</b> - 플래시 애니메이션 기술과 웹 서버 애플리케이션 기술을 통합하여 기존 HTML보다 역동적이고 인터랙티브한 웹페이지를 제공하는 플래시 웹페이지 제작기술이다</p>

<p><b>ISP / 인터넷 서비스 제공자</b> - 개인이나 회사들에게 인터넷 접속 서비스, 웹사이트 구축 및 웹호스팅 서비스 등을 제공하는 회사들</p>

<p><b>해시태그 </b> - #(특수기호)와 특정 단어를 붙여 쓴것으로, 소셜미디어에서 특정 핵심어를 편리하게 검색할 수 있도록 하는 메타데이터의 한 형태</p>

<p><b>AR / 증강현실</b> - 실제 촬영한 화면에 가상의 정보를 부가하여 보여주는 기술로, 혼합현실(MR) 이라고도 부른다.</p>

<p><b>CNAME </b> - 호스트 컴퓨터는 웹 접속을 위해 하나의 IP 주소를 가져야하는데, 이 IP주소는 DNS가 호스트 컴퓨터의 도메인 이름을 변환하여 생성한다. 그러나 종종 여러 개의 도메인 이름을 동일한 하나의 IP 주소로 변환하여 사용하기도하는데 이를 <b>CNAME</b> 이라고 한다.</p>

<p><b>HVC / 고성능 영상 코딩</b> - ISO/IEC JTC1 MPEG에서 AVC 이후의 표준으로 추진 중인 차세대 비디오 부호화 표준</p>

<p><b>Trackback / 트랙백 </b> - 다른사람의 글을 읽고 그 글에 직접 댓글을 올리는 대신에 내 블로그에 글을 올리고 내 글의 일정 부분이 다른 사람의 댓글로 보이도록 <b>트랙백</b> 핑을 보내는 것을 말한다</p>

<p><b>Webaholism / 웨바홀리즘 </b> - 웹과 알코올의 합성어로 일상생활에 지장을 느낄 정도로 지나치게 인터넷에 몰두하고, 인터넷에 접속하지 않으면 불안감을 느끼는 것으로 인터넷 중독증 또는 인터넷 의존증이라고 한다.</p>

<p><b>loT / 사물인터넷 </b> - 정보통신기술을 기반으로 실세계와 가상세계의 다양한 사물들을 연결하여 진보된 서비스를 제공하기 위한 서비스 기반 시설</p>

