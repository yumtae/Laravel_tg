		<p>18. <b>Zombie</b> - 다른 프로그램이나 다른 사용자를 조종하도록 악성코드에 감염된 컴퓨터를 말한다, 이러한 컴퓨터는 C&C(Command & Control) 서버의 제어를 받아 주로 Ddos공격에 이용된다</p>
