		<p>20. <b>악성 소프트웨어(Malware)</b> - 악의적인 목적을 위해 작성된 것으로 악성코드 또는 악성 프로그램 등으로 불린다. </p>
		<p>21. <b>스턱스넷(stuxnet)</b> - 독일 지멘스사의 원격 감시 제어 시스템의 제어 소프트웨어에 침투하여 시스템을 마비하게 할 목표로 제작된 악성코드, 산업기반 시설에 사용되는 제어 시스템에 침투하여 오작동 유도</p>
