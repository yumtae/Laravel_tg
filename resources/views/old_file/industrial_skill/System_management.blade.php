		<p>1. <b>프라이버시 강화기술</b> (PET : Pivacy Enhancing Technology) -
			개인정보 위험관리 기술 암호화,익명화 등 개인정보를 보호하는 기술에서 사용자가 직접 개인정보를 통제하기 위한 기술까지 다양한 사용자 프라이버시 보호기술을 통칭 
		</p>
			
	
	
		<p>2. <b>Digital Forensics (디지털 포렌식) </b>- 범죄의 증거로 사용될 수 있는 컴퓨터, 휴대전화, 인터넷 등의 디지털 저장매체에 존재하는 디지털 정보를 수집하는 디지털 수사 과정
		</p>
	
		
		<p>3. <b>DRM (Digital Rights Management, 디지털 저작권 권리)</b>- 디지털 매체를 통해 유통되는 데이터의 저작권을 보호하기 위한 시스템으로써, 데이터를 암호화해 인증된 사용자만 접속할 수 있게하거나, 워터마크 등 지적재산권을 보호
		</p>
	
		
		<p>4. <b>ONS(Object Naming Service)</b>- 사물에 관한 구체적인 정보가 저장되어 있는 서버의 위치를 알려주는 서비스.
		</p>
	
		<p>5. <b>친입방지시스템(IDS , Intrusion Detection System)</b>- 비정상적인 사용,오용,납용 등 실시간으로 탐지하는 시스템, 내-외부 정보의 흐름을 실시간으로 차단하기 위해 해커 친입 패턴에 대한 추적과 유해정보감시가 필요
		</p>
	
	
		<p>5. <b>전파식별(RFID, Radio Frequencty IDentification)</b> - 라디오 주파수 인식 기술로서, ic칩과 무선을 통해 식품,동물,사물등 다양한 개체의 정보를 관리할 수 있는 차세대 인식 기술이다</p>
		<p>6. <b>저작권(Copyright)</b>- 원저작물의 창작자가 저작물의 사용과 배포에 있어 일반적으로 제한된 시간 동안 배타적 권리를 인정하는 법적인 권리. 베타적권리는 저작권법에서 정한 제한 및 예외 사항에 의해 제한되므로 절대적인 권리는 아니다</p>
		<p>7. <b>Copyleft</b> - 저작권에 반대되는 개념으로써 지식이나 정보가 독점되어서는 안된다며 누구나 지식이나 정보에 접근할 수 있도록 열려 있어야 주장한다. 대표적인예로는 리눅스가 있다.</p>
		<p>8. <b>데이터 유출 방지(DLP, Data Leakage/Loss Prevention)</b> - 내부정보 유출방지 솔루션이다. 사내 직원이 모든 정보를 검색하고 사용자의행위를 탐지,통제해 외부로의 유출을 사전에 막는다</p>
		<p>9. <b>CC(Common Criteria, 공동평가기준)</b>- 1999년 6월8일 ISO 15408표준으로 채택된 정보 보호 제품 평가 기준이다. 정보화 순기능 역할을 보장하기 위해 정보화 제품의 정보보호 기능과 이에 대한 사용 환경 등급을 정한 기준이다.</p>
		<p>10. <b>SSL (Secure Sockets Layer)</b> 데이터를 송_수신하는 두 컴퓨터 사이, 종단 간 즉 TCP/IP계층과 애플리케이션 계층 사이에 위치하여 인증,암호화,무결성을 보장하는 업계 표준 프로토콜이다.</p>
		<p>11. <b>프록시 서버(Proxy Server)</b> - PC사용자와 인터넷 사이에서 중개자 역할을 하는 서버로 크게 방화벽 기능과 캐시기능을 수행한다.
			<ul>
				<li>방화벽 기능 : 컴퓨터 시스템에 방화벽을 설치하는 경우 외부와 연결하여 통신이 가능하도록 지원하며 HTTP,FTP,Gopher 프로토콜을 지원함</li>
				<li>캐시 기능: 많은 요청이 발생하는 데이터를 프록시 서버에 저장해두었다가 요청이 있을 경우 신속하게 전송</li>
			</ul>
		</p>
		<p>12. <b>HDCP(Hight-bandwidth Digital Content Protecion, 고대역폭 디지털 콘텐츠 전송 보호)</b> - 디지털 비주얼 인터페이스(DVI) 송_수신간 고대역폭의 비디오 암호화 전송을 보호하기 위한 규격.</p>
		<p>13. <b>빅데이터</b> - 기존의 관리 방법이나 분석체계로는 처리하기 어려운 막대한 양의 정형 또는 비정형 데이터 집합으로, 스마트 단말의 빠른 확산, 소셜 네트워크 서비스의 활성화, 사물 네트워크의 확대로 데이터 폭발이 더욱 가속화되고있다. 주목받는 이유는 기업이나 정부,포탈 등 <b>빅데이터</b>를 효과적으로 분석함으로써 미래를 예측해 최적의 대응 방안을 찾고, 이를 수익으로 연결하기 위함</p>
		<p>14. <b>CCL(Creative Commons License, 저작물 이용약관)</b>- 저작권자가 자신의 저작물에 대한 이용방법 및 조건을 표기하는 저작물 이용약관이다, web2.0기반의 환경이 환설화되면서 새롭게 대두되는 저작권 문제를 해결하기 위한 대안으로 등장</p>
		<p>15. <b>개인정보 영향평가 제도(PIA, Privacy Impact Assessment)</b> 개인정보를 활요하는 새로운 정보시스템의 도입 및 기존 정보시스템의 중요한 변경 시 시스템의 구축_운영의 기업의 고객은 물론 국민의 사생활에 미칠 영향에 대해 미리 조사,분석,평가하는 제도</p>
		<p>16. <b>목표 복구 시점(RPO , Recovery point Objecct)</b> - 조직에서 발생한 여러 가지 재난 상황으로, IT시스템이 마비되었을 때 각 업무에 필요한 데이터를 복구할 수 있는 기준점</p>
		<p>17. <b>목표 복구 시간(RTO, Recovery Time Objective)</b> - 비상사태 또는 업무 중단 시점부터 업무를 복귀하기 위한 목표 시간을 말한다</p>
		<p>18. <b>인터넷 제어 메시지 프로토콜(ICMP , Internet Control Message Protocol)</b>- TCP/IP 기반의 인터넷 통신 서비스에서 인터넷 프로토콜(IP)과 조합하여 통신 중에 발생하는 오류의 처리와 전송 경로의 변경 드을 위한 제어 메시지를 취급하는 프로토콜</p>
		<p>19. <b>WEB(Wired Equivalent Privacy, 유선급 프라이버시)</b> - 유선 랜에서 기대할 수 있는 것과 같은 보안과 프라이버시 수준이 제공되는 무선랜의 보안 프로토콜, 유선 랜은 건물 접근금지와 같은 물리적 보안체계로 되어 있지만, 무선랜은 유선망의 보안 수준에 맞춰 데이터를 암호화 한 것이다.</p>
		<p>21. <b>WPA(Wi0Fi Protected Access)</b> - Wi-Fi에서 제정한 무선 랜 인증 및 암호화 관련 표준, 패킷 당 키 할당가능, 키 값 재설정 등 다양한 기능이 있기 떄문에 해킹이 불가능하고 네트워크에 접근할 때 인증 절차를 요구</p>
		<p>22. <b>IP 보안 프로토콜 (IPSec, IP security protocol)</b>- 안전한 통신을 위한 통신 규약, 인터넷 상에서 전용 회선과 같이 이용 가능한 가상적인 전용회선을 구축해 데이터 도청 등의 행위를 방지하기 위한 통신 규약</p>
		<p>23. <b>OTP(One-Time Password, 일회용 패스워드)</b> - 로그인 할 때마다 그 세션에서만 사용할 수 있는 1회성 패스워드를 생성하는 보안 시스템.</p>
		<p>24. <b>SSO(Single Sign On)</b> - 한 번의 로그인으로 개인이 가입한 모든 사이트를 이용할 수 있게 해주는 시스템</p>
		<p>25. <b>공인인증서</b> - 전자 서명법에 의한 공인 인증 기관이 발행한 인증서</p>
		<p>26. <b>생체 인식</b> - 신체적, 행동적 특징을 자동화된 장치로 추출하고 분석하여 정확하게 개인의 신원을 확인하는 기술</p>
