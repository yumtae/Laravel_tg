		<p>24. <b>Splogger(스폴로거)</b> - 스팸과 블로거의 합성어로 다른 사람의 콘텐츠를 무단으로 복사해 자신의 블로그에 게재하는 블로거 또는 제품 광고나 음란물을 유포한 광고성 블로가</p>
		<p>25. <b>IP스푸핑(IP Spoofing)</b> - 다른 시스템과의 신뢰 관계를 속여 친입하는 크래킹 기술, 속이기라는 의미의 <b>스푸핑</b>은 기술적인 요소가 많이 필요한 크래킹 기술로 목표 호스트와 신뢰 관계를 맺고 있는 다른 호스트로 공격해 IP주소를 속여서 패킷을 보냄</p>
		<p>26. <b>스니핑(Sniffing)</b> - 네트우크 중간에서 남의 패킷 정보를 도청하는 해킹 유형의 하나로 수동적 공격, 네트워크 내의 패킷들은 대부분 암호화가 되어있지않아 스니핑 같은 해킹 기법에 취약하다.</p>
		<p>27. <b>스위치 재밍</b> - 위조된 MAX주소를 네트워크 상으로 지속적으로 흘려보내 스위칭 허브의 주소 테이블 기능을 마비시키는것. 스위칭 허브는 넘쳐나는 MAC 주소로 인해 주소 테이블이 오버플로되면 주소 테이블을 재설정하기위해 네트워크상의 컴퓨터들에게 MAC주소를 확인해 주소테이블을 새롭게 생성하는데, 이과정에서 해커의 MAC주소도포함시킨다</p>
		<p>28. <b>스팸</b> - 인터넷 상에서 다수의 수신인에게 무작위로 송신된 이메일 메시지를 의미.</p>
		<p>29. <b>사이버 스토킹</b> - 정보통신망을 이용해 악의적인 의도로 지속적으로 공포감이나 불안감등을 유발하는 행위.</p>
		<p>30. <b>피싱</b> - 허위 웹사이트를 내세워 사용자의 개인 신용정보를 뺴내는 수법</p>
		<p>31. <b>반달리즘</b> - 다수가 참여할 수 있도록 공개된 문서의 내용을 훼손하거나 엉뚱한 제목으로 변경하고 낙서를 하는 일 .  유럽 중세시대의 민족이동 당시에 악평이 자자하던 반달족의 무자비한 로마문화 파괴 및 약탈에 비유하는 말</p>
		<p>32. <b>살라미 기법</b> - 장기간 보관하여 두고 조금씩 얇게썰어서 먹는 이탈리아 소시지에서 따온 말로, 많은 대상으로부터 눈치 채지 못할 만큼의 적은 금액이나 양을 빼내는 사기기법.</p>
		<p>33. <b>SQL injection (SQL 삽입) 공격</b> - 웹사이트를 무차별적으로 공격하는 과정에서 취약한 사이트가 발견되면  데이터베이스 등의 데이터를 조작하는 일련의 공격방식</p>
		<p>34. <b>burte force attack (무작위 공격)</b> - 암호화된 문서의 암호키를 찾아내기 위해 적용 가능한 모든 값을 대입하여 공격하는 방법</p>
		<p>35. <b>Qshing(큐싱)</b> - QR코드를 통해 악성 앱의 다운로드를 유도하거나 악성 프로그램을 설치하도록 하는 금융사기 기법의 하나이다.</p>
		<p>36. <b>다크 데이터(Dark Data)</b>  - 수집된 후 저장은 되어 있지만 분석에 활용되지는 않는 다량의 데이터를 의미</p>
		<p>37. <b>블록체인</b> - P2P 네트워크를 이용해 온라인 금융 거래 정보를 온라인 네트워크 참여자의 디지털 장비에 분선 저장하는 기술</p>
		<p>38. <b>스누핑(Snooping)</b> - 네트워크상에서 남의 정보를 염탐하여 불법으로 가로채는 행위, 원격으로 다른 컴퓨터의 정보를 엿보면서 개인적인 내용,전자우편 등의 정보를 몰래 획득</p>
		<p>39. <b>다이어 악성코드(Dyre Malware)</b> - 인터넷 뱅킹 정보를 탈취하는 악성코드로, 트로이 목마의 한 종류. 주로 메일형태로 유포되 실행 시 컴퓨터 정보를 수집하고 보안프로그램을 무효화 하는 프로그램이 설치된다. 이후 공격 대상 사이트에 방문하면 유사 사이트로 연결해 개인정보를 탈취한다.</p>
		<p>40. <b>드롭퍼(Dropper)</b> - 정상적인 파일에 트로이 목마나 웜, 바이러스가 숨겨진 형태를 일컫는 말</p>
		<p>41. <b>메모리 해킹</b> - 컴퓨터 메모리에 있는 데이터를 위/변조 하는 해킹방법으로, 정상적인 인터넷 뱅킹 사이트를 이용했음에도 이체거래 과정에서 수취인의 계좌번호를 변조하거나 보안카다의 비밀번호를 뺴내어 돈을 빼돌린다</p>
		<p>42. <b>스미싱(Smishing)</b> - 문자 메시지와 피싱 합성어로, 무료쿠폰이나 모바일 초대장등의 문자메시지를 보낸 후 주소를 클릭하면 악성코드를 설치해 개인 정보를 뺴냄</p>
		<p>43. <b>스파이웨어(Spyware)</b> - 사용자 동의 없이 사용자 정보를 수집하는 프로그램, 또는 불편을 야기하거나 사생황을 침해할 수 있는 프로그램</p>
		<p>44. <b>웜(Warm)</b> - 네트워크를 통해 연속적으로 자신으르 복제하여 시스템 부하를 높여 결국 시스템을 다운시키는 바이러스의 일종</p>
		<p>45. <b>크래킹(Cracking)</b> - 어떤 목적을 가지고 타인의 시스템에 불법으로 침입하여 정보를 파괴하거나 정보의 내용을 자신의 이익에 맞게 변경하는 행위</p>
		<p>46. <b>트로이 목마(Trojan Horse)</b> - 정상적인 기능을 하는 프로그램으로 가장하여 프로그램 내에 숨어 있다가 해당 프로그램이 동작할 때 활성화되어 부작용을 일으키는 것</p>
		<p>47. <b>해킹(Hacking)</b> - 불법적으로 접근,침투 하여 시스템과 데이터를 파괴하는 행위</p>
		<p>48. <b>혹스(Hoax)</b> - 실제로 악성코드로 행동하지 않으면서 겉으로는 악성코드인것 처럼 가장하여 행동하는 소프트웨어</p>
			
	
