		<ul>
			<li>기업의 프로세스가 환경의 변화에 따라, 혹은 비용과 사이클 타임의 감소 등 개선을 목적으로 변화해야 할 떄 유연하고 신속하게 변화를 수용할 수 있도록 해준다.</li>
			<li>BPM을 시스템적으로 구현하는 핵심 수단은 워크플로우다</li>
			
		</ul>
		<div style="font-weight:bold">
			- BPM을 활용한 업무 프로세스 관리
			<ul>
				<li>기업 내외의 업무 프로세스들을 명시적으로 정의함으로써 가시화한다</li>
				<li>정의된 대로 업무가 수행될 수 있도록 관련된 사람과 시스템을 관리해 효율적인 업무수행 환경을 구축</li>
				<li>적잘한 평가기준과 지표에 따라 프로세스를 분석할 수 있는 정보를 제공해 개선방향을 인식 하게 해줌</li>
				<li>프로세스 정의 정보를 모델로 관리, 유연하게 변화를 수용할 수 있도록 해준다</li>
			
			</ul>
		</div>
	
	
	</p>
	
	
	
