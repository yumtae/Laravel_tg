	<h2>업무프로세스 재설계</h2><br/><br/><br/>
	
	
	<p>1. <b> BPR (Business Process Reengineering,업무 프로세스 재설계)의 정의 </b> <br/><br/>  경쟁에서 우위를 확보하기 위해 기업의 핵심 부문인 비용,품질,서비스,스피드와 같은 요인들이 획기적인 향상을 이룰 수 있도록, 프로세스를 기반으로 비즈니스 시스템을 근본적으로 재설계하여 혁신적인 성과를 추구하는 것
	
		<ul>
			<li>기업이 가진 IT 시스템의 가치를 높이고, 업무 품질을 개선하여 비용을 감소시키며, 경쟁 회사에 대한 우위를 확보하기위해 데이터의 흐름이 끊어지거나,부적절,중복, 정보처리의 병목현상이나 에러를 유발시키는업무를 찾아내 재설계를 통해 새로운 시스템 구현</li>
			
		</ul>
		<div style="font-weight:bold">
			- BPR(Business Process Reengineering)의 실행 4단계
			<ul>
				<li>1단계 : 현행 업무 프로세스 모형 분석</li>
